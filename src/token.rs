#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Comma,
    OpeningParenthesis,
    ClosingParenthesis,
    Equal,
    NotEqual,
    LessThan,
    LessThanOrEqual,
    GreaterThan,
    GreaterThanOrEqual,
    Plus,
    Minus,
    Multiply,
    Divide,
    NumberLiteral { value: i16 },
    StringLiteral { value: Vec<u8> },
    Variable { identifier: u8 },
    Print,
    If,
    Then,
    Goto,
    Input,
    Let,
    GoSub,
    Return,
    Clear,
    List,
    Run,
    End,
    EndOfFile,
}
